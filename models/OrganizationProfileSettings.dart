class OrganizationProfileSettings {
  final dynamic enabled;
  final String? description;
  final dynamic featured_projects;
  final dynamic featured_organizations;
  final dynamic links;
  final dynamic subscribe;
  final String? accent_color;

  OrganizationProfileSettings({
    required this.enabled,
    required this.description,
    required this.featured_projects,
    required this.featured_organizations,
    required this.links,
    required this.subscribe,
    required this.accent_color,
  });
}
